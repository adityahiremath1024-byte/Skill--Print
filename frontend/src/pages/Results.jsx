/* ─── Metric Card ───────────────────────────────────────────── */
function MetricCard({ icon, title, value, sub, color }) {
  return (
    <div className="card text-center">
      <span className="text-lg block mb-1">{icon}</span>
      <p className="text-[9px] text-[var(--color-text-faint)] uppercase tracking-wider" style={{ fontFamily: 'var(--font-mono)' }}>{title}</p>
      <p className="text-xl text-white mt-1" style={{ fontFamily: 'var(--font-display)', fontWeight: 500, color }}>{value}</p>
      {sub && <p className="text-[10px] text-[var(--color-text-muted)] mt-0.5">{sub}</p>}
    </div>
  );
}

/* ─── Skill Bar ─────────────────────────────────────────────── */
function SkillBar({ name, yours, required, index }) {
  const gap = Math.max(0, required - yours);
  const sev = gap >= 0.3 ? 'danger' : gap >= 0.15 ? 'ember' : gap >= 0.05 ? 'neon' : 'success';
  const c = { danger: 'var(--color-danger)', ember: 'var(--color-ember)', neon: 'var(--color-neon)', success: 'var(--color-success)' };

  return (
    <motion.div
      initial={{ opacity: 0, x: -12 }} animate={{ opacity: 1, x: 0 }} transition={{ delay: 0.15 + index * 0.03 }}
      className="grid grid-cols-12 gap-3 items-center py-2 border-b border-[var(--color-abyss-border)] last:border-b-0"
    >
      <span className="col-span-3 text-xs text-white" style={{ fontFamily: 'var(--font-display)', fontWeight: 500 }}>{name}</span>
      <div className="col-span-6 relative h-[4px] bg-[var(--color-abyss)] rounded-full overflow-hidden">
        <div className="absolute top-0 left-0 h-full rounded-full bg-[var(--color-abyss-border)]" style={{ width: `${required * 100}%` }} />
        <motion.div initial={{ width: 0 }} animate={{ width: `${yours * 100}%` }} transition={{ duration: 0.6, delay: 0.15 + index * 0.03 }}
          className="absolute top-0 left-0 h-full rounded-full" style={{ background: c[sev] }} />
      </div>
      <span className="col-span-1 text-center text-[10px]" style={{ fontFamily: 'var(--font-mono)', color: c[sev] }}>{(yours * 100).toFixed(0)}%</span>
      <div className="col-span-2 text-right">
        {gap > 0
          ? <span className={`badge text-[10px] badge-${sev === 'ember' ? 'high' : sev === 'danger' ? 'critical' : sev === 'neon' ? 'medium' : 'low'}`}>−{(gap * 100).toFixed(0)}%</span>
          : <span className="badge badge-low text-[10px]">✓</span>}
      </div>
    </motion.div>
  );
}

/* ─── Download Report ───────────────────────────────────────── */
function generateReportText(gapData, allSkillNames, matchScore, avgMastery, bktUpdates) {
  const lines = [
    '╔══════════════════════════════════════════════╗',
    '║          SKILLPRINT AI — ANALYSIS REPORT      ║',
    '╚══════════════════════════════════════════════╝',
    '', `Date: ${new Date().toLocaleDateString()}`, '',
    `Match Score: ${(matchScore * 100).toFixed(0)}%`,
    `Average Mastery: ${(avgMastery * 100).toFixed(0)}%`,
    `Skills Analyzed: ${allSkillNames.length}`,
    `Critical Gaps: ${gapData.filter(g => g.gap >= 0.3).length}`,
    '', '─── SKILL BREAKDOWN ───', '',
  ];
  gapData.forEach(g => {
    const status = g.gap >= 0.3 ? '🔴 CRITICAL' : g.gap >= 0.15 ? '🟡 HIGH' : g.gap > 0 ? '🟢 GAP' : '✅ MET';
    lines.push(`${g.name.padEnd(20)} │ Yours: ${(g.yours * 100).toFixed(0).padStart(3)}% │ Required: ${(g.required * 100).toFixed(0).padStart(3)}% │ Gap: ${g.gap > 0 ? '-' : ' '}${(g.gap * 100).toFixed(0).padStart(2)}% │ ${status}`);
  });
  if (bktUpdates.length > 0) {
    lines.push('', '─── DIAGNOSTIC LOG ───', '');
    bktUpdates.forEach(u => {
      lines.push(`${u.correct ? '✓' : '✗'} ${u.skill.padEnd(20)} │ ${u.prior.toFixed(2)} → ${u.posterior.toFixed(2)} │ ${u.assessment || ''}`);
    });
  }
  lines.push('', '─── RECOMMENDED FOCUS AREAS ───', '');
  gapData.filter(g => g.gap > 0.1).slice(0, 5).forEach((g, i) => {
    lines.push(`${i + 1}. ${g.name} — close ${(g.gap * 100).toFixed(0)}% gap (~${Math.round(g.gap * 50)} hours)`);
  });
  lines.push('', '───────────────────────────────', 'Generated by SkillPrint AI');
  return lines.join('\n');
}

/* ─── RESULTS PAGE ──────────────────────────────────────────── */
export default function Results() {
  const navigate = useNavigate();
  const { skills, jdRequirements, bktUpdates, skillMastery, matchScore } = useSkillPrintStore();

  const allSkillNames = useMemo(() => [...new Set([...skills.map(s => s.name), ...jdRequirements.map(s => s.name)])], [skills, jdRequirements]);
  const yourScores = useMemo(() => allSkillNames.map(n => { const b = skillMastery[n]; if (b !== undefined) return b; const s = skills.find(x => x.name === n); return s ? s.decayed_confidence : 0; }), [allSkillNames, skillMastery, skills]);
  const requiredScores = useMemo(() => allSkillNames.map(n => { const r = jdRequirements.find(x => x.name === n); return r ? r.raw_confidence : 0; }), [allSkillNames, jdRequirements]);

  const gapData = useMemo(() => allSkillNames.map((n, i) => ({
    name: n, yours: yourScores[i], required: requiredScores[i], gap: Math.max(0, requiredScores[i] - yourScores[i])
  })).sort((a, b) => b.gap - a.gap), [allSkillNames, yourScores, requiredScores]);

  const criticalGaps = gapData.filter(g => g.gap >= 0.3).length;
  const avgMastery = yourScores.length ? yourScores.reduce((a, b) => a + b, 0) / yourScores.length : 0;
  const strongest = gapData.reduce((m, g) => g.yours > m.yours ? g : m, gapData[0]);
  const totalHours = gapData.reduce((s, g) => s + Math.round(g.gap * 50), 0);

  const downloadReport = () => {
    const txt = generateReportText(gapData, allSkillNames, matchScore, avgMastery, bktUpdates);
    const blob = new Blob([txt], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url; a.download = 'skillprint-report.txt'; a.click();
    URL.revokeObjectURL(url);
  };

  const radarData = {
    labels: allSkillNames,
    datasets: [
      { label: 'Required', data: requiredScores, borderColor: '#00f5d4', backgroundColor: 'rgba(0,245,212,0.06)', borderWidth: 1.5, pointRadius: 2, pointBackgroundColor: '#00f5d4' },
      { label: 'Your SkillPrint', data: yourScores, borderColor: '#f59e0b', backgroundColor: 'rgba(245,158,11,0.06)', borderWidth: 1.5, pointRadius: 2, pointBackgroundColor: '#f59e0b' },
    ],
  };

  const radarOpts = {
    responsive: true, maintainAspectRatio: false,
    animation: { duration: 900 },
    scales: { r: { beginAtZero: true, max: 1, ticks: { stepSize: 0.25, color: '#374151', backdropColor: 'transparent', font: { size: 8, family: "'JetBrains Mono'" } }, grid: { color: 'rgba(255,255,255,0.03)' }, angleLines: { color: 'rgba(255,255,255,0.03)' }, pointLabels: { color: '#9ca3af', font: { size: 10, family: "'DM Sans'" } } } },
    plugins: { legend: { position: 'bottom', labels: { color: '#9ca3af', padding: 12, font: { size: 11, family: "'DM Sans'" }, usePointStyle: true, pointStyleWidth: 5 } }, tooltip: { backgroundColor: '#111827', titleColor: '#f9fafb', bodyColor: '#9ca3af', borderColor: 'rgba(0,245,212,0.15)', borderWidth: 1, cornerRadius: 6, titleFont: { family: "'Space Grotesk'" }, bodyFont: { family: "'JetBrains Mono'", size: 10 }, callbacks: { label: (c) => `${c.dataset.label}: ${(c.raw * 100).toFixed(0)}%` } } },
  };

  return (
    <div className="min-h-screen bg-[var(--color-abyss)]" style={{ paddingTop: '56px' }}>

      {/* Header */}
      <section style={{ padding: '24px 0 16px' }}>
        <div className="container-main flex flex-col md:flex-row md:items-center md:justify-between gap-3">
          <div>
            <p className="text-[var(--color-neon)] text-[10px] tracking-widest uppercase mb-1" style={{ fontFamily: 'var(--font-mono)' }}>Analysis Complete</p>
            <h1 style={{ fontSize: 'clamp(24px, 3.5vw, 36px)' }}>Your <span className="gradient-text">SkillPrint</span></h1>
            <p className="text-[var(--color-text-muted)] text-sm mt-1">{allSkillNames.length} skills analyzed · {criticalGaps} critical gaps</p>
          </div>
          <div className="flex gap-2">
            <button onClick={downloadReport} className="btn-ghost !text-xs">📥 Download Report</button>
            <button onClick={() => navigate('/learning-path')} className="btn-primary !text-xs">Learning Path →</button>
          </div>
        </div>
      </section>

      {/* Metrics row */}
      <section className="section-alt" style={{ padding: '16px 0' }}>
        <div className="container-main grid grid-cols-2 md:grid-cols-5 gap-2">
          <MetricCard icon="📊" title="Match" value={`${(matchScore * 100).toFixed(0)}%`} color="var(--color-neon)" sub="Role fit" />
          <MetricCard icon="🧠" title="Mastery" value={`${(avgMastery * 100).toFixed(0)}%`} color="var(--color-ember)" sub={`${allSkillNames.length} skills`} />
          <MetricCard icon="🔴" title="Critical" value={criticalGaps.toString()} color="var(--color-danger)" sub="Gaps" />
          <MetricCard icon="⏱" title="Hours" value={totalHours.toString()} color="var(--color-signal)" sub="To close gaps" />
          <MetricCard icon="🏆" title="Strongest" value={strongest?.name || '—'} color="var(--color-success)" sub={strongest ? `${(strongest.yours * 100).toFixed(0)}%` : ''} />
        </div>
      </section>

      {/* Radar + Key Findings */}
      <section style={{ padding: '20px 0' }}>
        <div className="container-main grid md:grid-cols-12 gap-4">
          <motion.div initial={{ opacity: 0, scale: 0.97 }} animate={{ opacity: 1, scale: 1 }} transition={{ delay: 0.15 }}
            className="md:col-span-7 card">
            <div className="h-[280px] md:h-[320px]"><Radar data={radarData} options={radarOpts} /></div>
          </motion.div>

          <div className="md:col-span-5 flex flex-col gap-3 reveal-stagger">
            <div className="card-glow reveal">
              <h3 className="text-xs text-white mb-2" style={{ fontFamily: 'var(--font-display)', fontWeight: 500 }}>Key Findings</h3>
              {gapData.slice(0, 4).map(g => (
                <div key={g.name} className="flex items-center justify-between py-1.5 border-b border-[var(--color-abyss-border)] last:border-b-0">
                  <span className="text-xs text-white">{g.name}</span>
                  <span className={`badge text-[10px] ${g.gap >= 0.3 ? 'badge-critical' : g.gap >= 0.15 ? 'badge-high' : 'badge-medium'}`}>
                    {g.gap > 0 ? `−${(g.gap * 100).toFixed(0)}%` : '✓'}
                  </span>
                </div>
              ))}
            </div>

            {strongest && (
              <div className="card reveal">
                <p className="text-[9px] text-[var(--color-text-faint)] uppercase tracking-wider mb-1" style={{ fontFamily: 'var(--font-mono)' }}>Biggest Opportunity</p>
                <h3 className="text-sm text-white mb-1" style={{ fontFamily: 'var(--font-display)', fontWeight: 500 }}>{gapData[0]?.name}</h3>
                <p className="text-[10px] text-[var(--color-text-muted)] mb-2">
                  At {(gapData[0].yours * 100).toFixed(0)}% — needs {(gapData[0].required * 100).toFixed(0)}%. ~{Math.round(gapData[0].gap * 50)}h to close.
                </p>
                <div className="relative h-1.5 bg-[var(--color-abyss)] rounded-full overflow-hidden">
                  <div className="absolute top-0 left-0 h-full rounded-full bg-[var(--color-abyss-border)]" style={{ width: `${gapData[0].required * 100}%` }} />
                  <motion.div initial={{ width: 0 }} animate={{ width: `${gapData[0].yours * 100}%` }} transition={{ duration: 0.8 }}
                    className="absolute top-0 left-0 h-full rounded-full bg-[var(--color-danger)]" />
                </div>
              </div>
            )}

            {bktUpdates.length > 0 && (
              <div className="card reveal">
                <p className="text-[9px] text-[var(--color-text-faint)] uppercase tracking-wider mb-1" style={{ fontFamily: 'var(--font-mono)' }}>
                  Diagnostic · {bktUpdates.length} answered
                </p>
                <div className="flex flex-col gap-0.5 max-h-[100px] overflow-y-auto">
                  {bktUpdates.map((u, i) => (
                    <div key={i} className="flex items-center gap-1.5 text-[10px]" style={{ fontFamily: 'var(--font-mono)' }}>
                      <span style={{ color: u.correct ? 'var(--color-success)' : 'var(--color-danger)' }}>{u.correct ? '✓' : '✗'}</span>
                      <span className="text-white truncate flex-1">{u.skill}</span>
                      <span className="text-[var(--color-text-faint)]">{u.prior.toFixed(2)}→{u.posterior.toFixed(2)}</span>
                    </div>
                  ))}
                </div>
              </div>
            )}
          </div>
        </div>
      </section>

      {/* Full breakdown */}
      <section className="section-alt" style={{ padding: '20px 0 32px' }}>
        <div className="container-main">
          <div className="reveal mb-4">
            <p className="text-[var(--color-neon)] text-[10px] tracking-widest uppercase mb-1" style={{ fontFamily: 'var(--font-mono)' }}>Breakdown</p>
            <h2 style={{ fontSize: 'clamp(20px, 2.5vw, 28px)' }}>Skill-by-Skill <span className="gradient-text">Analysis</span></h2>
          </div>
          <div className="card !p-0 overflow-hidden reveal">
            <div className="grid grid-cols-12 gap-3 px-4 py-2 border-b border-[var(--color-abyss-border)] text-[9px] text-[var(--color-text-faint)] uppercase tracking-wider" style={{ fontFamily: 'var(--font-mono)' }}>
              <span className="col-span-3">Skill</span><span className="col-span-6">Mastery vs Required</span><span className="col-span-1 text-center">Score</span><span className="col-span-2 text-right">Gap</span>
            </div>
            <div className="px-4">{gapData.map((g, i) => <SkillBar key={g.name} name={g.name} yours={g.yours} required={g.required} index={i} />)}</div>
          </div>
        </div>
      </section>
    </div>
  );
}
